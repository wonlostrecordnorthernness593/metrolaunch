  function tilePx(tile) {
    const s = TILE_SIZES[tile.size];
    return {
      x: tile.col * (cellSize + GRID_GAP),
      y: tile.row * (cellSize + GRID_GAP),
      w: s.cols * cellSize + (s.cols - 1) * GRID_GAP,
      h: s.rows * cellSize + (s.rows - 1) * GRID_GAP,
    };
  }

  function getGridHeight() {
    let maxRow = 0;
    tiles.forEach(t => {
      maxRow = Math.max(maxRow, t.row + TILE_SIZES[t.size].rows);
    });
    return maxRow * (cellSize + GRID_GAP) + 120;
  }

  function hexToRgba(hex, opacity01) {
    const c = hex.replace('#', '');
    const r = parseInt(c.substring(0, 2), 16);
    const g = parseInt(c.substring(2, 4), 16);
    const b = parseInt(c.substring(4, 6), 16);
    return `rgba(${r},${g},${b},${opacity01})`;
  }

