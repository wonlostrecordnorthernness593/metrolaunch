  // COLLISION
  function overlaps(a, b) {
    const sa = TILE_SIZES[a.size], sb = TILE_SIZES[b.size];
    return !(a.col + sa.cols <= b.col || b.col + sb.cols <= a.col ||
      a.row + sa.rows <= b.row || b.row + sb.rows <= a.row);
  }

  function canPlace(tile, col, row, excludeId) {
    const s = TILE_SIZES[tile.size];
    if (col < 0 || col + s.cols > GRID_COLS || row < 0) return false;
    for (const t of tiles) {
      if (t.id === excludeId) continue;
      if (overlaps({ ...tile, col, row }, t)) return false;
    }
    return true;
  }

  function pushTilesAway(changedId) {
    const changed = tiles.find(t => t.id === changedId);
    if (!changed) return;
    let moved = true;
    let iterations = 0;
    while (moved && iterations < 100) {
      moved = false;
      iterations++;
      for (const t of tiles) {
        if (t.id === changedId) continue;
        if (overlaps(changed, t)) {
          const sc = TILE_SIZES[changed.size];
          t.row = changed.row + sc.rows;
          moved = true;
        }
      }
      if (moved) {
        for (let i = 0; i < tiles.length; i++) {
          for (let j = i + 1; j < tiles.length; j++) {
            if (tiles[i].id === changedId || tiles[j].id === changedId) continue;
            if (overlaps(tiles[i], tiles[j])) {
              const si = TILE_SIZES[tiles[i].size];
              if (tiles[j].row < tiles[i].row + si.rows) {
                tiles[j].row = tiles[i].row + si.rows;
              }
            }
          }
        }
      }
    }
  }

  function compactGrid() {
    if (!settings.gridlock) return;
    const sorted = tiles.slice().sort((a, b) => a.row - b.row || a.col - b.col);
    let changed = true;
    let iters = 0;
    while (changed && iters < 200) {
      changed = false;
      iters++;
      for (const t of sorted) {
        // TILE_SIZES[t.size] not needed here; canPlace reads tile.size internally
        let bestRow = t.row;
        for (let r = 0; r < t.row; r++) {
          if (canPlace(t, t.col, r, t.id)) { bestRow = r; break; }
        }
        if (bestRow < t.row) {
          t.row = bestRow;
          changed = true;
        }
      }
    }
  }

  function findNextFreeSpot(size) {
    const s = TILE_SIZES[size];
    for (let row = 0; row < 200; row++) {
      for (let col = 0; col <= GRID_COLS - s.cols; col++) {
        if (canPlace({ size }, col, row, null)) return { col, row };
      }
    }
    return { col: 0, row: 0 };
  }

