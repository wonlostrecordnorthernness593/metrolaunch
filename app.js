  // DRAG AND DROP
  function initDrag(tileId, startX, startY, isResize) {
    const tile = tiles.find(t => t.id === tileId);
    if (!tile) return;
    const el = gridEl.querySelector(`[data-id="${tileId}"]`);
    if (!el) return;

    const px = tilePx(tile);
    const rect = gridEl.getBoundingClientRect();

    const snapshot = tiles.map(t => ({ id: t.id, col: t.col, row: t.row }));
    dragState = {
      tileId, startX, startY,
      origCol: tile.col, origRow: tile.row, origSize: tile.size,
      offsetX: startX - rect.left - px.x,
      offsetY: startY - rect.top - px.y,
      isResize, moved: false,
      snapshot,
    };

    if (!isResize) el.classList.add('dragging');
    scrollEl.classList.add('dragging-active');

    let ph = gridEl.querySelector('.drop-placeholder');
    if (!ph) { ph = document.createElement('div'); ph.className = 'drop-placeholder'; gridEl.appendChild(ph); }
    Object.assign(ph.style, { left: `${px.x}px`, top: `${px.y}px`, width: `${px.w}px`, height: `${px.h}px` });
  }

  function moveDrag(clientX, clientY) {
    if (!dragState) return;
    dragState.moved = true;
    const tile = tiles.find(t => t.id === dragState.tileId);
    if (!tile) return;
    const el = gridEl.querySelector(`[data-id="${dragState.tileId}"]`);
    if (!el) return;
    const rect = gridEl.getBoundingClientRect();

    if (dragState.isResize) {
      const relX = clientX - rect.left;
      const relY = clientY - rect.top;
      const originX = tile.col * (cellSize + GRID_GAP);
      const originY = tile.row * (cellSize + GRID_GAP);
      const spanC = Math.max(1, Math.round((relX - originX) / (cellSize + GRID_GAP)));
      const spanR = Math.max(1, Math.round((relY - originY) / (cellSize + GRID_GAP)));

      let ns = tile.size;
      if (spanC >= 4 && spanR >= 4) ns = 'large';
      else if (spanC >= 3 && spanR <= 2) ns = 'wide';
      else if (spanC >= 2 && spanR >= 2) ns = 'medium';
      else ns = 'small';

      if (isSpecialTile(tile)) {
        if (ns !== 'medium' && ns !== 'wide') ns = tile.size;
      }

      if (ns !== tile.size) {
        const s = TILE_SIZES[ns];
        if (tile.col + s.cols <= GRID_COLS) {
          tile.size = ns;
          pushTilesAway(tile.id);
          compactGrid();
          save();
          render();
          return;
        }
      }
      return;
    }

    const newX = clientX - rect.left - dragState.offsetX;
    const newY = clientY - rect.top - dragState.offsetY;

    el.style.left = `${newX}px`;
    el.style.top = `${newY}px`;
    el.style.transition = 'none';

    const snapCol = clamp(Math.round(newX / (cellSize + GRID_GAP)), 0, GRID_COLS - TILE_SIZES[tile.size].cols);
    const snapRow = Math.max(0, Math.round(newY / (cellSize + GRID_GAP)));

    const s = TILE_SIZES[tile.size];
    const prevSnap = { col: dragState._snapCol, row: dragState._snapRow };
    if (snapCol !== prevSnap.col || snapRow !== prevSnap.row) {
      // restore other tiles to snapshot before computing new displacement
      if (dragState.snapshot) {
        dragState.snapshot.forEach(snap => {
          if (snap.id === tile.id) return;
          const t = tiles.find(x => x.id === snap.id);
          if (t) { t.col = snap.col; t.row = snap.row; }
        });
      }

      const origCol = tile.col, origRow = tile.row;
      tile.col = snapCol;
      tile.row = snapRow;
      pushTilesAway(tile.id);
      tile.col = origCol;
      tile.row = origRow;

      const ph = gridEl.querySelector('.drop-placeholder');
      if (ph) {
        ph.style.left = `${snapCol * (cellSize + GRID_GAP)}px`;
        ph.style.top = `${snapRow * (cellSize + GRID_GAP)}px`;
        ph.style.width = `${s.cols * cellSize + (s.cols - 1) * GRID_GAP}px`;
        ph.style.height = `${s.rows * cellSize + (s.rows - 1) * GRID_GAP}px`;
      }
      dragState._snapCol = snapCol;
      dragState._snapRow = snapRow;

      gridEl.style.height = `${getGridHeight()}px`;
      tiles.forEach(ot => {
        if (ot.id === tile.id) return;
        const oel = gridEl.querySelector(`[data-id="${ot.id}"]`);
        if (oel) {
          const op = tilePx(ot);
          oel.style.left = `${op.x}px`;
          oel.style.top = `${op.y}px`;
        }
      });
    }
  }

  function endDrag() {
    if (!dragState) return;
    const tile = tiles.find(t => t.id === dragState.tileId);
    const el = gridEl.querySelector(`[data-id="${dragState.tileId}"]`);

    if (tile && dragState.moved && !dragState.isResize && dragState._snapCol != null) {
      tile.col = dragState._snapCol;
      tile.row = dragState._snapRow;
      pushTilesAway(tile.id);
      compactGrid();
    }
    save();

    if (el) { el.classList.remove('dragging'); el.style.transition = ''; }
    scrollEl.classList.remove('dragging-active');
    const ph = gridEl.querySelector('.drop-placeholder');
    if (ph) ph.remove();
    gridEl.style.height = `${getGridHeight()}px`;

    const wasMoved = dragState.moved;
    dragState = null;
    render();
    return wasMoved;
  }

  // INPUT HANDLERS
  function setupInput() {
    let touchTile = null;
    let touchStart = 0;
    let touchStartX = 0;
    let touchStartY = 0;
    let touchMoved = false;
    let launchGuard = 0;

    function tileFrom(e) {
      const el = e.target.closest('.tile');
      return el ? tiles.find(t => t.id === el.dataset.id) : null;
    }
    function xy(e) {
      return e.touches ? { x: e.touches[0].clientX, y: e.touches[0].clientY } : { x: e.clientX, y: e.clientY };
    }
    function isHandle(e) { return !!e.target.closest('.resize-handle'); }
    function isBadge(e) { return !!e.target.closest('.edit-badge'); }

    gridEl.addEventListener('touchstart', (e) => {
      hideContextMenu();
      const tile = tileFrom(e);
      if (!tile) return;
      if (editMode && isBadge(e)) return;
      const p = xy(e);
      touchTile = tile;
      touchStart = Date.now();
      touchStartX = p.x;
      touchStartY = p.y;
      touchMoved = false;

      if (editMode) {
        e.preventDefault();
        initDrag(tile.id, p.x, p.y, isHandle(e));
        return;
      }
      clearTimeout(longPressTimer);
      longPressTimer = setTimeout(() => {
        if (!editMode) toggleEditMode();
        initDrag(tile.id, p.x, p.y, false);
      }, LONG_PRESS_MS);
    }, { passive: false });

    gridEl.addEventListener('touchmove', (e) => {
      if (dragState) { e.preventDefault(); const p = xy(e); moveDrag(p.x, p.y); }
      else {
        clearTimeout(longPressTimer);
        const p = xy(e);
        if (Math.abs(p.x - touchStartX) > 8 || Math.abs(p.y - touchStartY) > 8) {
          touchMoved = true;
        }
      }
    }, { passive: false });

    gridEl.addEventListener('touchend', (e) => {
      clearTimeout(longPressTimer);
      let m = false;
      if (dragState) { m = endDrag(); }
      if (touchTile && !touchMoved && !m && Date.now() - touchStart < LONG_PRESS_MS) {
        if (Date.now() - launchGuard < 600) { touchTile = null; return; }
        launchGuard = Date.now();
        if (editMode) {
          const cx = e.changedTouches ? e.changedTouches[0].clientX : (window.innerWidth / 2);
          const cy = e.changedTouches ? e.changedTouches[0].clientY : (window.innerHeight / 2);
          showContextMenu(touchTile.id, cx, cy);
        } else {
          if (isWeatherTile(touchTile)) {
            const url = touchTile.url || 'weather://';
            playLaunchAnimation(touchTile, (t) => {
              if (t.forceSafari) window.open(url, '_blank');
              else window.location.href = url;
            });
          }
          else if (isNewsTile(touchTile)) {
            const item = currentNewsItem();
            if (item?.url) { playLaunchAnimation(touchTile, (t) => window.open(item.url, '_blank')); }
            else { showToast('No headlines loaded'); }
          }
          else if (isSpotifyTile(touchTile)) {
            playLaunchAnimation(touchTile, (t) => { window.location.href = 'spotify://'; });
          }
          else launchApp(touchTile);
        }
      }
      touchTile = null;
    });

    gridEl.addEventListener('touchcancel', () => { clearTimeout(longPressTimer); if (dragState) endDrag(); touchTile = null; });

    let mouseDown = false;
    let mouseMoved = false;
    gridEl.addEventListener('mousedown', (e) => {
      hideContextMenu();
      const tile = tileFrom(e);
      if (!tile || e.button === 2) return;
      if (editMode && isBadge(e)) return;
      mouseDown = true; mouseMoved = false; touchTile = tile; touchStart = Date.now();
      if (editMode) { initDrag(tile.id, e.clientX, e.clientY, isHandle(e)); return; }
      clearTimeout(longPressTimer);
      longPressTimer = setTimeout(() => { if (!editMode) toggleEditMode(); initDrag(tile.id, e.clientX, e.clientY, false); }, LONG_PRESS_MS);
    });

    window.addEventListener('mousemove', (e) => {
      if (!mouseDown) return;
      if (dragState) moveDrag(e.clientX, e.clientY);
      else { clearTimeout(longPressTimer); mouseMoved = true; }
    });

    window.addEventListener('mouseup', (e) => {
      if (!mouseDown) return;
      mouseDown = false;
      clearTimeout(longPressTimer);
      let m = false;
      if (dragState) { m = endDrag(); }
      if (touchTile && !mouseMoved && !m && Date.now() - touchStart < LONG_PRESS_MS) {
        if (Date.now() - launchGuard < 600) { touchTile = null; return; }
        launchGuard = Date.now();
        if (editMode) {
          showContextMenu(touchTile.id, e.clientX, e.clientY);
        } else {
          if (isWeatherTile(touchTile)) {
            const url = touchTile.url || 'weather://';
            playLaunchAnimation(touchTile, (t) => {
              if (t.forceSafari) window.open(url, '_blank');
              else window.location.href = url;
            });
          }
          else if (isNewsTile(touchTile)) {
            const item = currentNewsItem();
            if (item?.url) { playLaunchAnimation(touchTile, (t) => window.open(item.url, '_blank')); }
            else { showToast('No headlines loaded'); }
          }
          else if (isSpotifyTile(touchTile)) {
            playLaunchAnimation(touchTile, (t) => { window.location.href = 'spotify://'; });
          }
          else launchApp(touchTile);
        }
      }
      touchTile = null;
    });

    gridEl.addEventListener('contextmenu', (e) => {
      e.preventDefault();
      const tile = tileFrom(e);
      if (tile) showContextMenu(tile.id, e.clientX, e.clientY);
    });

    scrollEl.addEventListener('click', (e) => {
      if ((e.target === scrollEl || e.target === gridEl) && editMode) toggleEditMode();
    });

    document.addEventListener('click', (e) => { if (!e.target.closest('.context-menu')) hideContextMenu(); });
  }

