// prevent rubber-banding or overscroll on iOS globally
document.addEventListener('touchmove', (e) => {
  // let range sliders work natively
  if (e.target.closest('input[type="range"]')) return;
  const scrollEl = e.target.closest('.grid-scroll, .modal-sheet, .scrollable-y');

  if (scrollEl) {
    if (scrollEl.classList.contains('dragging-active')) {
      e.preventDefault();
      return;
    }

    // if perfectly fit, prevent all scrolling
    if (Math.ceil(scrollEl.scrollHeight) <= Math.ceil(scrollEl.clientHeight)) {
      e.preventDefault();
      return;
    }

    // prevent bounce at the very top
    if (Math.ceil(scrollEl.scrollTop) <= 0 && e.targetTouches && e.targetTouches[0]) {
      const touch = e.targetTouches[0];
      if (!scrollEl._startY) scrollEl._startY = touch.clientY;
      if (touch.clientY > scrollEl._startY) { // dragging down while at top
        e.preventDefault();
      }
    }

    // prevent bounce at the very bottom
    if (Math.ceil(scrollEl.scrollTop + scrollEl.clientHeight) >= Math.floor(scrollEl.scrollHeight) && e.targetTouches && e.targetTouches[0]) {
      const touch = e.targetTouches[0];
      if (!scrollEl._startY) scrollEl._startY = touch.clientY;
      if (touch.clientY < scrollEl._startY) { // dragging up while at bottom
        e.preventDefault();
      }
    }
  } else {
    // touching the header, background layer, or outside scrollable content prevents whole-page bounce
    e.preventDefault();
  }
}, { passive: false });

document.addEventListener('touchstart', (e) => {
  const scrollEl = e.target.closest('.grid-scroll, .modal-sheet, .scrollable-y');
  if (scrollEl && e.targetTouches && e.targetTouches[0]) {
    scrollEl._startY = e.targetTouches[0].clientY;
  }
}, { passive: true });

